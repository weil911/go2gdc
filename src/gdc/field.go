//EndpointFieldsMapping gets endpoint fields info by "endpoint/_mapping".
func EndpointFieldsMapping(s0Endpoint string) (b1Body []byte) {
	s0Endpoint = s0Endpoint + "/_mapping"
	b1Body = EndpointRecordsDefault(s0Endpoint)
	return
}

//EndpointFieldsAll returns all endpoint fields by "endpoint/_mapping".
func EndpointFieldsAll(s0Endpoint string) (s1Field []string) {
	b1Body := EndpointFieldsMapping(s0Endpoint)
	s1Field = strings.FieldsFunc(strings.Trim(jsoniter.Get(b1Body, "fields").ToString(), `[] "`),
		func(r rune) bool {
			return !(unicode.IsLetter(r) || unicode.IsNumber(r) || r == '_' || r == '.')
		})
	return
}

//EndpointFieldsSelected returns selected endpoint fields.
func EndpointFieldsSelected(s0Endpoint string) (s1Field []string) {
	s1Field = make([]string, 0)
	s1FieldExcluded := EndpointFieldsExcluded(s0Endpoint)
	s1FieldEndpoint := EndpointFieldsAll(s0Endpoint)
	for _, s0Field := range s1FieldEndpoint {
		bool0Excluded := false
		for _, s0FieldExcluded := range s1FieldExcluded {
			if s0Field == s0FieldExcluded {
				bool0Excluded = true
				break
			}
		}
		if !bool0Excluded {
			s1Field = append(s1Field, s0Field)
		}
	}
	return
}

